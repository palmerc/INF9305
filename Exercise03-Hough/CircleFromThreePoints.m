function [xy, r] = CircleFromThreePoints(points)

x1 = points(1, 1);
y1 = points(2, 1);
x2 = points(1, 2);
y2 = points(2, 2);
x3 = points(1, 3);
y3 = points(2, 3);

a = det([x1 y1 1; ...
         x2 y2 1; ...
         x3 y3 1]);
d = -det([x1.^2 + y1.^2 y1 1; ...
          x2.^2 + y2.^2 y2 1; ...
          x3.^2 + y3.^2 y3 1]);
e = det([x1.^2 + y1.^2 x1 1; ...
         x2.^2 + y2.^2 x2 1; ...
         x3.^2 + y3.^2 x3 1]);
f = -det([x1.^2 + y1.^2 x1 y1; ...
          x2.^2 + y2.^2 x2 y2; ...
          x3.^2 + y3.^2 x3 y3]);
 
xy(:) = [-d / (2 * a); -e / (2 * a)];
r = sqrt((d.^2 + e.^2) / (4 * a.^2) - f / a);